export function getProducto(id){
    return new Promise((resolve) => {
        console.log('realizando consulta todos los productos')
        setTimeout(()=>{
            let producto = data.find( (elemento)=> {
                return parseInt(elemento.id) === id
            })
            resolve(producto)
        }, 2000)
        
    })
}
export function getProductoCategory(category){
    return new Promise((resolve) => {
        console.log('realizando consulta por categoria')
        setTimeout(()=>{
            let categoryProductos = data.filter( (elemento) => {
                return elemento.categoria.toLocaleLowerCase() === category;
            })
            resolve(categoryProductos)
        }, 2000)
        
    })
}
